# numpy supports exponential as well as log operations
import numpy as np

a=np.array([(1,2,3),(3,4,5)])

print("========================================")

print(np.exp(a))

print("========================================")

print(np.log(a))  # natural log

print("========================================")

print(np.log10(a)) # actual log

